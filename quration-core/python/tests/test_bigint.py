def test_str():
    a = BigInt(5)
    assert "5" == a.to_str()
    assert "5" == str(a)


def test_comparison():
    a = BigInt(5)
    b = BigInt(1)
    assert a == 5
    assert 5 == a
    assert a == b + 4
    assert b + 4 == a
    assert a != 4
    assert 4 != a
    assert a != b + 5
    assert b + 5 != a


def test_int_to_bool_array():
    assert [] == int_as_bool_array(0)
    assert [True, False, True] == int_as_bool_array(5)
    assert [True, True, True] == int_as_bool_array(7)
    assert [False, False, False, True] == int_as_bool_array(8)


def test_bigint_to_bool_array():
    assert [] == int_as_bool_array(BigInt(0))
    assert [True, False, True] == int_as_bool_array(BigInt(5))
    assert [True, True, True] == int_as_bool_array(BigInt(7))
    assert [False, False, False, True] == int_as_bool_array(BigInt(8))


def test_bool_array_to_int():
    assert 0 == bool_array_as_int([])
    assert 5 == bool_array_as_int([True, False, True])
    assert 7 == bool_array_as_int([True, True, True])
